console.log("Rounding off :");
console.log(Math.round(3.14)); // Output: 3
console.log(Math.round(3.9)); // Output: 4

console.log("Calculating floor :");
console.log(Math.floor(3.14)); // Output: 3
console.log(Math.floor(3.9)); // Output: 3

console.log("Calculating ceilling :");
console.log(Math.ceil(3.14)); // Output: 4
console.log(Math.ceil(3.9)); // Output: 4

console.log("Calculating absolute value :");
console.log(Math.abs(-3)); // Output: 3
console.log(Math.abs(3)); // Output: 3

console.log("Calculating maximum value :");
console.log(Math.max(3, 5, 1)); // Output: 5
console.log(Math.max(-3, 5, 1)); // Output: 5

console.log("Calculating minimum value :");
console.log(Math.min(3, 5, 1)); // Output: 1
console.log(Math.min(-3, 5, 1)); // Output: -3

console.log("Calculating square root :");
console.log(Math.sqrt(4)); // Output: 2
console.log(Math.sqrt(16)); // Output: 4

console.log("Calculating powers :");
console.log(Math.pow(2, 3)); // Output: 8
console.log(Math.pow(3, 2)); // Output: 9

console.log("Generating random number :");
console.log(Math.random()); // Output: A random number between 0 and 1 (exclusive)
