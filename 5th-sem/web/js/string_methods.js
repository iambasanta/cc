console.log("Concatenation to strings :");
const str1 = "Hello";
const str2 = "World";
console.log(str1.concat(" ", str2)); // Output: Hello World

console.log("Find the index of o in Hello World:");
console.log(str.indexOf("o")); // Output: 4

console.log("Find the last index of o in Hello world:");
console.log(str.lastIndexOf("o")); // Output: 7

console.log("Replace string:");
console.log(str.replace("World", "JavaScript")); // Output: Hello JavaScript

console.log("Slice String:");
console.log(str.slice(0, 5)); // Output: Hello

console.log("Split String by , :");
console.log(str.split(",")); // Output: ["Hello", "World"]

console.log("Change the string to lowercase:");
console.log(str.toLowerCase()); // Output: hello world

console.log("Change the string to uppercase:");
console.log(str.toUpperCase()); // Output: HELLO WORLD

